# Reference Genome and Annotation

Download reference genome (FASTA) and GTF annotation files:

## Example for human GRCh38:
- Genome FASTA: ftp://ftp.ensembl.org/pub/release-109/fasta/homo_sapiens/dna/Homo_sapiens.GRCh38.dna.primary_assembly.fa.gz
- Annotation GTF: ftp://ftp.ensembl.org/pub/release-109/gtf/homo_sapiens/Homo_sapiens.GRCh38.109.gtf.gz

Place genome in: `ref/genome.fa`
Place annotation in: `ref/annotation.gtf`
